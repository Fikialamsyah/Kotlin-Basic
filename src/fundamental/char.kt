package fundamental

fun main() {
    // Characters direpresentasikan menggunakan tipe Char ('')
    var vocal = 'A'

    // increment pada char
    println("Vocal " + vocal++)
    println("Vocal " + vocal++)
    println("Vocal " + vocal++)
    // decrement pada char
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
}